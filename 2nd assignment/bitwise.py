a = 5  # In binary: 0101
b = 3  # In binary: 0011

# AND
print(a & b)  # Output: 1 (0001 in binary)

# OR
print(a | b)  # Output: 7 (0111 in binary)

# XOR
print(a ^ b)  # Output: 6 (0110 in binary)

# NOT
print(~a)  # Output: -6 (inverts all bits, two's complement representation)

# Left shift
print(a << 1)  # Output: 10 (binary: 1010)

# Right shift
print(a >> 1)  # Output: 2 (binary: 0010)
