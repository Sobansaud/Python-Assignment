x = 10 
y = 5

# Assign the value of x to y
y = x  
print("Value of y:", y)

# Add and assign
x += 5  
print("x after addition:", x) 

# Subtract and assign
y -= 3  
print("y after subtraction:", y)

# Multiply and assign
x *= 2 
print("x after multiplication:", x) 

# Divide and assign
x /= 3  
print("x after division:", x) 

# Modulus and assign
x %= 4 
print("x after modulus:", x)  

# Exponent and assign
x **= 3 
print("x after exponentiation:", x) 
